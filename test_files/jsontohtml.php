    foreach ($default as $value){
        ksort($value);//sorts products in ascending order
    }

    //Converting JSON to html elements
    foreach ($default as $defaultcategory => $value){//each default category
        $html_out .= "<div class=\"filter-defaultcategory\" id =\"filter-defaultcategory-{$defaultcategory}\">\n<h2>{$defaultcategory}</h2>\n";
        foreach ($value as $uniqueid => $product){//each product
            $html_out .= "<div class=\"filter-product\" id=\"filter-product-{$uniqueid}\">\n";
            foreach ($product as $key => $field){//each field
                $html_out .= array_to_html($key,$field); //outputs html
            }
            $html_out .="</div>\n";
        }
        $html_out .= "</div>\n";
    }
    $html_out .= "</div>\n";

    return $html_out;
}

/**
 * @param $input_key
 * @param $input_array
 * @return string
 */
function array_to_html($input_key,$input_array){
    $output = '';
    switch(explode("_",$input_key)[0]){
        case "DefaultCategory":
            break;
        case "Link":
            $output .= "<div class = 'filter-link'>\n<p>\n";
            $output .= "<b>{$input_array['bold']}</b>\n";
            $output .= "<a href=\"{$input_array['href']}\" target= \"{$input_array['target']}\">{$input_array['alttext']}</a>\n";
            $output .= "</p>\n</div>\n";
            break;
        case "Photo":

            $output .= "<div class = 'filter-photo'>\n";
            $output .= "<img src=\"{$input_array['src']}\" alt=\"{$input_array['alt']}\">\n";
            $output .= "</div>\n";

            break;
        case "Category":
            foreach ($input_array as $key=>$category){
                if($key != 'hidden'){
                    $output .= "<div class=\"filter-category-{$key}\">\n";
                    $output .= "<p>\n<b>{$key}: </b>\n";
                    foreach ($category as $key => $group){//each group
                        $output .= ($key == count($category)-1)?"{$group}":"{$group},";//with or without comma
                    }
                    $output .= "</p>\n</div>\n";
                }
            }
            break;
        default:
            $output .= "<div class ='filter-text'>\n";
            $output .= "<p>\n<b>{$input_array['bold']}</b>\n{$input_array['text']}</p>\n";
            $output .= "</div>\n";
            break;
    }
    return $output;
}
